package actividad8.modelo;

import java.io.Serializable;

/**
 *
 * @author josue
 */
public class ModeloLibro implements Serializable {
    private String titulo;
    private String autor;
    private int anio;

    public ModeloLibro(String titulo, String autor, int anio) {
        this.titulo = titulo;
        this.autor = autor;
        this.anio = anio;
    }

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    public String getAutor() {
        return autor;
    }

    public void setAutor(String autor) {
        this.autor = autor;
    }

    public int getAnio() {
        return anio;
    }

    public void setAnio(int anio) {
        this.anio = anio;
    }
    
}
